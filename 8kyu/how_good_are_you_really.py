# В вашем классе прошел тест, и вы его успешно сдали. Поздравляем!
# Но вы — амбициозный человек. Вы хотите узнать, лучше ли ваш
# результат по сравнению со средним результатом учеников в вашем классе.
# Вам предоставляется массив с результатами тестов ваших одноклассников.
# Вычислите средний балл и сравните его с вашим результатом!
# Верните True, если ваш результат лучше среднего, иначе — False.
# Примечание:
# Ваш результат не включен в массив с баллами вашего класса.
# Не забудьте учесть ваш результат при сравнении со средним баллом!


# Вариант решения:
def better_than_average(class_points, your_points):
    # Рассчитать средний балл класса
    average_score = sum(class_points) / len(class_points)
    # Сравнить ваш балл со средним
    return your_points > average_score


# Тесты
print(better_than_average([50, 60, 70], 75))  # True
print(better_than_average([90, 85, 80], 85))  # False
print(better_than_average([100, 90], 95))     # True
print(better_than_average([40, 50, 60], 50))  # False
print(better_than_average([10, 20, 30], 25))  # True